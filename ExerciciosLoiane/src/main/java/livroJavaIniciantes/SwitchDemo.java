package livroJavaIniciantes;

//demostração switch
public class SwitchDemo {
    public static void main(String args[]){
        int i;

        for( i = 0; i < 10; i++){
            switch(i){
                case 0:
                    System.out.println("i is zero");
                    break;
                case 1:
                    System.out.println("i is zero");
                    break;
                case 2:
                    System.out.println("i is zero");
                    break;
                case 3:
                    System.out.println("i is zero");
                    break;
                case 4:
                    System.out.println("i is zero");
                    break;
                default:
                    System.out.println("i is five or more");

            }
        }
    }
}
