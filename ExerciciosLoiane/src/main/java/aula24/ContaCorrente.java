package aula24;
/*
crie uma classe para representar uma conta corrente que possui um número, um saldo,
um status que informa se lea é especial ou não, um limite
 */

public class ContaCorrente {
    private String nome;
    private int id;
    private Double saldo;
    private boolean contaEspecial;

    public  ContaCorrente(String nome, int id, double saldo){
        this.id = id;
        this.nome = nome;
        this.saldo = saldo;
        this.contaEspecial = contaEspecial;
    }
}
